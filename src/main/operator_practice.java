package src.main;

public class operator_practice {
    public static void main(String args[]){
        //this is the arithmetic operator
        int a =10;
        int b =20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(a--);

        //assignment operator
        System.out.println(a+=1);
        System.out.println(a-=1);
        System.out.println(a*=1);
        System.out.println(a/=1);
        System.out.println(a%=1);
        System.out.println(a^=1);
        System.out.println(a>>=1);
        System.out.println(a<<=1);


    }
}
