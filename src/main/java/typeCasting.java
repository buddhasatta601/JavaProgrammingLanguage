public class typeCasting {

    //broadening casting
    public static void main(String [] args){
        int num1 =10;
        System.out.println(num1);
        long num2=num1;
        System.out.println(num2);
    //slimming casting

        int num3=10;
        byte num4 =(byte) num3;
        System.out.println(num4);
    }
}
